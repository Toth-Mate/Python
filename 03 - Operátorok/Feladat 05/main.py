
print("Adja meg az első számot: ")
x:float = float(input())

print("Adja meg a második számot: ")
y:float = float(input())

print("Adja meg a harmadik számot: ")
z:float = float(input())

print("Adja meg a negyedik számot: ")
zs:float = float(input())

eredmeny:float = (x + y) / (z - zs)
print(f"({x} + {y}) / ({z} - {zs}) = {eredmeny}")
